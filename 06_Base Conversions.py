# Base Conversions
# 1.bin(): To convert from any base to binary
print(bin(15)) #'0b1111'
print(bin(0o11)) #'0b1001'
print(bin(0X10)) #'0b10000'

# 2. oct(): To convert from any base to octal
print(oct(10)) #'0o12'
print(oct(0B1111)) #'0o17'
print(oct(0X123)) #'0o443'

# 3. hex(): To convert from any base to hexa decimal

print(hex(100)) #'0x64'
print(hex(0B111111)) #'0x3f'
print(hex(0o12345)) #'0x14e5'
